	public int compareTo(CalendarBooking calendarBooking) {
		int value = 0;

		value = DateUtil.compareTo(getStartDate(),
				calendarBooking.getStartDate());

		if (value != 0) {
			return value;
		}

		value = getTitle().toLowerCase()
					.compareTo(calendarBooking.getTitle().toLowerCase());

		if (value != 0) {
			return value;
		}

		return 0;
	}

